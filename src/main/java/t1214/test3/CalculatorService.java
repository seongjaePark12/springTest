package t1214.test3;

public class CalculatorService {
	public void add(int su1, int su2) {
		System.out.println(su1 +"+" +su2+ "="+(su1+su2));
	}
	public void sub(int su1, int su2) {
		System.out.println(su1 +"-" +su2+ "="+(su1-su2));
	}
	public void mul(int su1, int su2) {
		System.out.println(su1 +"*" +su2+ "="+(su1*su2));
	}
	public void div(int su1, int su2) {
		System.out.println(su1 +"/" +su2+ "="+(su1/su2));
	}
}
